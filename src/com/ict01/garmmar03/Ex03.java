package com.ict01.garmmar03;
class Ex03{
	public static void main(String[] args){
		int s1 = 7;
		s1 += 4;
		System.out.println("s1= "+s1);
		
		s1 -= 4;
		System.out.println("s1= "+s1);

		s1 *= 4;
		System.out.println("s1= "+s1);

		s1 /= 4;
		System.out.println("s1= "+s1);

		s1 %= 4;
		System.out.println("s1= "+s1);
		
		//(a = a+1) == (a += 1)  

	}
}