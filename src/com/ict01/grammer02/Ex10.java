package com.ict01.grammer02;
class Ex10{
	public static void main(String[] args){
	//증감연산자 : 1증가 혹은 감소 연산, char, 정수, 실수
	//++변수, 변수++: 1순위냐 최후순위냐차이
	int s1 = 10;
	int s2 = 10;
	
	System.out.println(++s1 +2);
	System.out.println(s2++ +2);
	System.out.println();
	System.out.println("s1="+s1);
	System.out.println("s2="+s2);
	
	}
}